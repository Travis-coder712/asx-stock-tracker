# Map period start dates to constituent lists
PEER_GROUPS = {
    '2023-07-01': ASX100_JUN_2023,
    '2024-07-01': ASX100_JUN_2024,
    '2025-07-01': ASX100_JUN_2025,
}


def get_constituent_changes():
    """Track which companies entered/left the ASX 100 each year."""
    years = [
        ('Jun 2023', set(ASX100_JUN_2023)),
        ('Jun 2024', set(ASX100_JUN_2024)),
        ('Jun 2025', set(ASX100_JUN_2025)),
    ]
    changes = []
    for i in range(1, len(years)):
        label_prev, prev = years[i - 1]
        label_curr, curr = years[i]
        entered = sorted(curr - prev)
        exited = sorted(prev - curr)
        changes.append({
            'from': label_prev,
            'to': label_curr,
            'entered': entered,
            'exited': exited,
        })
    return changes


if __name__ == '__main__':
    for c in get_constituent_changes():
        print(f"\n{c['from']} → {c['to']}:")
        print(f"  Entered ({len(c['entered'])}): {', '.join(t.replace('.AX','') for t in c['entered'])}")
        print(f"  Exited  ({len(c['exited'])}):  {', '.join(t.replace('.AX','') for t in c['exited'])}")
